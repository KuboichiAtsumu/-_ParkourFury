#include "minigameback.h"

//===========================================================================================================
// コンストラクタ
//===========================================================================================================
CMiniGameBack::CMiniGameBack() : CBack3D()
{
}

//===========================================================================================================
// デストラクタ
//===========================================================================================================
CMiniGameBack::~CMiniGameBack()
{
}

//===========================================================================================================
// 初期化処理
//===========================================================================================================
HRESULT CMiniGameBack::Init()
{
	//基底クラス初期化処理
	if (FAILED(CBack3D::Init()))
	{
		return E_FAIL;
	}

	return S_OK;
}

//===========================================================================================================
// 終了処理
//===========================================================================================================
void CMiniGameBack::Uninit()
{
	//基底クラス終了処理
	CBack3D::Uninit();
}

//===========================================================================================================
// 解放処理
//===========================================================================================================
void CMiniGameBack::Release()
{
	//基底クラス解放処理
	CBack3D::Release();
}

//===========================================================================================================
// 更新処理
//===========================================================================================================
void CMiniGameBack::Update()
{
	//基底クラス更新処理
	CBack3D::Update();
}

//===========================================================================================================
// 描画処理
//===========================================================================================================
void CMiniGameBack::Draw()
{
	//エディタモードの場合
	if (CManager::GetInstance()->GetScene()->GetSceneState() == CScene::SCENE::EDIT_3D)
	{//描画せずに処理を抜ける
		return;
	}

	//基底クラス描画処理
	CBack3D::Draw();
}
