#include "bossback.h"

//===========================================================================================================
// コンストラクタ
//===========================================================================================================
CBossBack::CBossBack() : CBack3D()
{
}

//===========================================================================================================
// デストラクタ
//===========================================================================================================
CBossBack::~CBossBack()
{
}

//===========================================================================================================
// 初期化処理
//===========================================================================================================
HRESULT CBossBack::Init()
{
	//基底クラス初期化処理
	if (FAILED(CBack3D::Init()))
	{
		return E_FAIL;
	}

	return S_OK;
}

//===========================================================================================================
// 終了処理
//===========================================================================================================
void CBossBack::Uninit()
{
	//基底クラス終了処理
	CBack3D::Uninit();
}

//===========================================================================================================
// 解放処理
//===========================================================================================================
void CBossBack::Release()
{
	//基底クラス解放処理
	CBack3D::Release();
}

//===========================================================================================================
// 更新処理
//===========================================================================================================
void CBossBack::Update()
{
	//基底クラス更新処理
	CBack3D::Update();
}

//===========================================================================================================
// 描画処理
//===========================================================================================================
void CBossBack::Draw()
{
	//エディタモードの場合
	if (CManager::GetInstance()->GetScene()->GetSceneState() == CScene::SCENE::EDIT_3D)
	{//描画せずに処理を抜ける
		return;
	}

	//基底クラス描画処理
	CBack3D::Draw();
}
