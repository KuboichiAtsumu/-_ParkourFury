#include "bosshpframe.h"

//===========================================================================================================
// コンストラクタ
//===========================================================================================================
CBossHPFrame::CBossHPFrame() : CUI()
{
}

//===========================================================================================================
// デストラクタ
//===========================================================================================================
CBossHPFrame::~CBossHPFrame()
{
}

//===========================================================================================================
// 初期化処理
//===========================================================================================================
HRESULT CBossHPFrame::Init()
{
	//基底クラス初期化処理
	if (FAILED(CUI::Init()))
	{
		return E_FAIL;
	}

	return S_OK;
}

//===========================================================================================================
// 終了処理
//===========================================================================================================
void CBossHPFrame::Uninit()
{
	//基底クラス終了処理
	CUI::Uninit();
}

//===========================================================================================================
// 更新処理
//===========================================================================================================
void CBossHPFrame::Update()
{
	//基底クラス更新処理
	CUI::Update();
}

//===========================================================================================================
// 描画処理
//===========================================================================================================
#include "game.h"
void CBossHPFrame::Draw()
{
	//プレイヤーがボスエリアにいない
	if (CGame::GetInstance()->GetArea() != CGame::GAME_AREA::BOSS) return;

	//基底クラス描画処理
	CUI::Draw();
}
